const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const packageJson = JSON.parse(
  fs.readFileSync(path.join(rootDir, 'package.json'), 'utf8')
);

const version = packageJson.version || '0.0.0';
const buildId = `${version}-${Date.now()}`;
const builtAt = new Date().toISOString();

const versionPayload = { version, buildId, builtAt };

/** Copied to dist via angular.json (public folder). */
const assetsDir = path.join(rootDir, 'public');
/** Imported at compile time in the Angular bundle. */
const constantsDir = path.join(rootDir, 'src/app/core/constants');

fs.mkdirSync(assetsDir, { recursive: true });
fs.mkdirSync(constantsDir, { recursive: true });

fs.writeFileSync(
  path.join(assetsDir, 'app-version.json'),
  `${JSON.stringify(versionPayload, null, 2)}\n`
);

const tsContent = `// Auto-generated by scripts/generate-app-version.js — do not edit manually
export const APP_VERSION = '${version}';
export const APP_BUILD_ID = '${buildId}';
export const APP_BUILT_AT = '${builtAt}';
`;

fs.writeFileSync(path.join(constantsDir, 'app-version.generated.ts'), tsContent);

console.log(`Generated app version ${version} (${buildId})`);

module.exports = { version, buildId, builtAt };
